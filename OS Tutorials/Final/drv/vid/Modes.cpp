modes::modes()
{
	char c = (*(unsigned short*)0x410&0x30);	//Detects video card type, vga or mono

	if(c == 0x30)			//c can be 0x00 or 0x20 for colour, 0x30 for mono
	{
		videomem = (unsigned short*) 0xb0000;
		crtc_mem = 0x3B4;	// mono
	}
	else
	{
		videomem = (unsigned short*) 0xb8000;
		crtc_mem = 0x3D4;	// colour
	}
}


void modes::write_regs(unsigned char *regs)
{
	unsigned index;

	outportb(crtc_mem, 17);					// enable writes to CRTC regs 0-7
	outportb((crtc_mem + 1),
	inportb((crtc_mem + 1)) & 0x7F);

	outportb(crtc_mem, 3);					// enable access to vertical retrace regs
	outportb((crtc_mem + 1),
	inportb((crtc_mem + 1)) | 0x80);

	// make sure we don't disable writes to the CRTC registers:
	regs[17] &= 0x7F;								// undo write-protect on CRTC regs 0-7
	regs[3] |= 0x80;								// enable access to vertical retrace regs
	for(index = 0; index < NUM_CRTC_REGS; index++)
	{
		outportb(crtc_mem, index);
		outportb((crtc_mem + 1), *regs);
		regs++;
	}

	regs += (NUM_AC_REGS + NUM_GC_REGS);			// attribute controller

	for(index = 0; index < NUM_SEQ_REGS; index++)	// sequencer
	{
		outportb(VGA_SEQ_INDEX, index);
		outportb(VGA_SEQ_DATA, *regs);
		regs++;
	}

	outportb(VGA_MISC_WRITE, *regs);				// other regs
}


void modes::outsw(unsigned short adr, unsigned short *data, unsigned count)
{
	for(; count != 0; count--)
	{
		outportw(adr, *data);
		data++;
	}
}


void modes::set_font(Font *font)
{
	/*	0x0100: synchronous reset
		0x0402: enable plane 4; disable planes 8, 2, and 1
		0x0704: ModeX off, even/odd mode ON (?), >64K on, text mode
		0x0300: undo synchronous reset */
	static unsigned short seq_set[] =
	{
		0x0100, 0x0402, 0x0704, 0x0300
	};

	/*	0x0204: read mode 0 reads from plane 4
		0x0005: 256-color mode off, even/odd mode OFF, read mode 0, write mode 0
		0x0C06: screen at B8000, even/odd mode OFF, text mode */
	static unsigned short gc_set[] =
	{
		0x0204, 0x0005, 0x0C06
	};

	/*	0x0100: synchronous reset
		0x0302: enable planes 1 and 2; disable planes 4 and 8
		0x0304: Mode X off, even/odd mode OFF, >64K on, text mode
		0x0300: undo synchronous reset */
	static unsigned short seq_reset[] =
	{
		0x0100, 0x0302, 0x0304, 0x0300
	};

	/*	0x0004: read mode 0 reads from none of the four planes
		0x1005: 256-color mode off, even/odd mode ON, read mode 0, write mode 0
		0x0C06: screen at B8000, even/odd mode ON, text mode */
	static unsigned short gc_reset[] =
	{
		0x0004, 0x1005, 0x0E06
	};

	unsigned short temp;
	unsigned long dst;
	unsigned char *src;

	// access font memory (plane 4) instead of text memory (planes 2 and 1)
	outsw(VGA_SEQ_INDEX, seq_set, sizeof(seq_set) / sizeof(unsigned short));

	outsw(VGA_GC_INDEX, gc_set, sizeof(gc_set) / sizeof(unsigned short));

	// program the font
	src = font->bitmaps + font->first * font->height;
	dst = font->first * 32;
	for(temp = font->first; temp < font->last; temp++)
	{
		memcpy((void *)(0xB8000L + dst), src, font->height);
		src += font->height;
		dst += 32;
	}

	// access text memory (planes 2 and 1) instead of font memory (plane 4)
	outsw(VGA_SEQ_INDEX, seq_reset, sizeof(seq_reset) / sizeof(unsigned short));
	outsw(VGA_GC_INDEX, gc_reset, sizeof(gc_reset) / sizeof(unsigned short));

	// set font height in CRTC register 9
	outportb(crtc_mem, 9);
	outportb((crtc_mem + 1), (inportb((crtc_mem + 1)) & 0xE0) | ((font->height - 1) & 0x1F));

	// set top and bottom scanlines of cursor
	if(font->height == 16)
	{
		outportb(crtc_mem, 10);
		outportb((crtc_mem + 1), 13);
		outportb(crtc_mem, 11);
		outportb((crtc_mem + 1), 14);
	}
	else if(font->height == 8)
	{
		outportb(crtc_mem, 10);
		outportb((crtc_mem + 1), 7);
		outportb(crtc_mem, 11);
		outportb((crtc_mem + 1), 7);
	}
}

void modes::setMode(Modes mode)					//Sets the video mode
{
	semStart();

	switch (mode)
	{
		case m8x25:
			write_regs(_mode_80x25);
			set_font(&_font_8x16);
			scrHeight = 25;
			scrWidth = 80;
			break;

		case m8x50:
			write_regs(_mode_80x50);
			set_font(&_font_8x8);
			scrHeight = 50;
			scrWidth = 80;
			break;

		case m9x60:
			write_regs(_mode_90x60);
			set_font(&_font_8x8);
			scrHeight = 60;
			scrWidth = 90;
	}

	semEnd();

	*(unsigned short *)0x44A = scrWidth;		//Add screen width + height into the bios data area
	*(unsigned char *)0x484 = scrHeight - 1;
}



